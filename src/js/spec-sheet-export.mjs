// ===== spec-sheet-export.mjs — ABB Smart Winch Spec Sheet PDF export =====
//
// Builds the field values client-side from analyzer state & computed model,
// then POSTs them to the server which fills the fillable PDF template.

import { q, read } from './utils.mjs';
import { collectInputState } from './persist-inputs.mjs';
import { apiUrl, apiHeaders } from './api-config.mjs';

const MM_PER_IN = 25.4;
const KGF_TO_LBF = 2.20462;
const MPM_TO_FPM = 3.28084;
const W_PER_HP = 745.7;

function safeRead(id) {
  try { return read(id); } catch { return null; }
}

function fmt(v, decimals = 2) {
  if (v == null || !Number.isFinite(v)) return '';
  return (+v.toFixed(decimals)).toString();
}

function dualIn(inches) {
  if (!Number.isFinite(inches)) return '';
  return `${fmt(inches * MM_PER_IN, 1)} mm / ${fmt(inches, 2)} in`;
}

function forceStr(kgf) {
  if (!Number.isFinite(kgf)) return '';
  return `${fmt(kgf, 0)} kg / ${fmt(kgf * KGF_TO_LBF, 0)} lbs`;
}

function speedStr(mpm) {
  if (!Number.isFinite(mpm)) return '';
  return `${fmt(mpm, 1)} mpm / ${fmt(mpm * MPM_TO_FPM, 1)} fpm`;
}

/**
 * Build the PDF field values from the current analyzer state + model.
 * @param {object|null} model - The last computed model from buildComputationModel()
 */
export function buildSpecSheetFields(model) {
  const projectName = q('project_name')?.value || '';
  const systemType = q('system_type_select')?.value || '';
  const winchType = q('winch_type_select')?.value || '';
  const today = new Date().toISOString().slice(0, 10);

  const coreDiaIn = safeRead('core_in');
  const ftfIn = safeRead('ftf_in');
  const cableMm = safeRead('c_mm');
  const efficiency = safeRead('system_efficiency');
  const ratedSwlKgf = safeRead('rated_swl_kgf');
  const ratedSpeedMpm = safeRead('rated_speed_mpm');
  const motors = safeRead('motors');
  const motorHp = safeRead('motor_hp');
  const motorMaxRpm = safeRead('motor_max_rpm');
  const gr1 = safeRead('gr1');
  const gr2 = safeRead('gr2');
  const totalLayers = model?.summary?.total_layers ?? null;
  const lightLoadKgf = Number.isFinite(ratedSwlKgf) ? ratedSwlKgf * 0.1 : NaN;
  const motorKw = Number.isFinite(motorHp) ? motorHp * W_PER_HP / 1000 : NaN;

  let maxSpeedAtLightLoad = NaN;
  if (model?.rows?.length) {
    const lastRow = model.rows[model.rows.length - 1];
    if (model.electricEnabled) maxSpeedAtLightLoad = lastRow?.el_speed_available_mpm ?? NaN;
    else if (model.hydraulicEnabled) maxSpeedAtLightLoad = lastRow?.hyd_speed_available_mpm ?? NaN;
  }

  const grTotal = (Number.isFinite(gr1) && Number.isFinite(gr2)) ? gr1 * gr2 : NaN;
  const depth = safeRead('depth_m');
  const payload = safeRead('payload_kg');
  const cableW = safeRead('c_w_kgpm');
  const mbl = safeRead('mbl_kgf');
  const sf = safeRead('safety_factor');

  const commentLines = [
    `Generated by C-LARS Winch Analyzer on ${today}`,
    '',
    `Drive Type: ${systemType === 'hydraulic' ? 'Hydraulic' : 'Electric'}`,
    `Winch Type: ${winchType === 'traction' ? 'Traction' : 'Conventional'}`,
    `Total Gear Ratio (GR1 x GR2): ${fmt(grTotal, 2)}`,
    `Operating Depth: ${fmt(depth, 0)} m`,
    `Payload in Water: ${fmt(payload, 0)} kg`,
    `Cable Weight in Water: ${fmt(cableW, 2)} kg/m`,
    `Min. Breaking Load: ${fmt(mbl, 0)} kgf`,
    `Safety Factor: ${fmt(sf, 1)}`,
  ];

  if (model?.hydraulicEnabled) {
    const hStrings = safeRead('h_pump_strings');
    const hHp = safeRead('h_emotor_hp');
    const hRpm = safeRead('h_emotor_rpm');
    const hPump = safeRead('h_pump_cc');
    const hPsi = safeRead('h_max_psi');
    const hMot = safeRead('h_hmot_cc');
    commentLines.push(
      '',
      'Hydraulic System:',
      `  Pump Strings: ${fmt(hStrings, 0)}`,
      `  E-Motor: ${fmt(hHp, 0)} HP @ ${fmt(hRpm, 0)} RPM`,
      `  Pump: ${fmt(hPump, 0)} cc/rev`,
      `  Max Pressure: ${fmt(hPsi, 0)} psi`,
      `  Hyd Motor: ${fmt(hMot, 0)} cc/rev`,
    );
  }

  // Text fields — keys must match the PDF form field names exactly
  const textFields = {
    'Customer Reference': projectName,
    'Dated': today,
    'Bare Drum Dia mm  in': dualIn(coreDiaIn),
    'Drum Length mm  in': dualIn(ftfIn),
    'Rope Dia mm  in': Number.isFinite(cableMm)
      ? `${fmt(cableMm, 1)} mm / ${fmt(cableMm / MM_PER_IN, 3)} in` : '',
    'System Efficiency  Usually 90 or over': Number.isFinite(efficiency)
      ? `${fmt(efficiency * 100, 0)}%` : '',
    'Gearbox Ratio ratio': fmt(gr1, 3),
    'Rated Load kg  lbs': forceStr(ratedSwlKgf),
    'Maximum Continuous Speed at Rated Load mpm  fpm': speedStr(ratedSpeedMpm),
    'Light Load kg  lbs Usually light load is 10 to 30 of the rated load': forceStr(lightLoadKgf),
    'Maximum Control Speed at Light Load mpm  fpm': speedStr(maxSpeedAtLightLoad),
    'Maximum or Final Layer to be Wound': Number.isFinite(totalLayers) ? String(totalLayers) : '',
    'No. Motors': Number.isFinite(motors) ? fmt(motors, 0) : '',
    'Motor Power': Number.isFinite(motorKw)
      ? `${fmt(motorKw, 1)} kW / ${fmt(motorHp, 1)} HP` : '',
    'Motor RPM': Number.isFinite(motorMaxRpm) ? fmt(motorMaxRpm, 0) : '',
    '9 Comments  Notes Supply drawing or sketch of winch mechanics  dimensions separately if requiredRow1': commentLines.join('\n'),
  };

  // Put GR2 info in external gearing field
  if (Number.isFinite(gr2) && gr2 > 0) {
    textFields['Small Gear Teeth teeth For gearing ratio calculations'] = `Ratio: ${fmt(gr2, 3)}`;
  }

  // Checkboxes to check
  const checkBoxes = ['Check Box - Layer 0', 'Check Box LARS'];

  return { textFields, checkBoxes, projectName, dated: today };
}

/**
 * Download a filled ABB Spec Sheet PDF from the server.
 * @param {object|null} model - The last computed model
 */
export async function downloadSpecSheetPDF(model) {
  const { textFields, checkBoxes, projectName, dated } = buildSpecSheetFields(model);

  const res = await fetch(apiUrl('/api/spec-sheet/pdf'), {
    method: 'POST',
    headers: { ...apiHeaders(), 'Content-Type': 'application/json' },
    body: JSON.stringify({ textFields, checkBoxes }),
  });

  if (!res.ok) {
    const err = await res.json().catch(() => ({ error: { message: res.statusText } }));
    throw new Error(err?.error?.message || `Server error ${res.status}`);
  }

  const blob = await res.blob();
  const safeName = (projectName || 'winch').replace(/[^a-zA-Z0-9_-]/g, '_').replace(/_+/g, '_');
  const filename = `${safeName}_ABB_SpecSheet_${dated}.pdf`;

  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}
